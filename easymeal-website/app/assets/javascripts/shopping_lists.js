function modif_alim(){
    $('#my_modal').on('show.bs.modal', function(e) {
	var alim_name = $(e.relatedTarget).data('alim-name');
	var alim_quantity = $(e.relatedTarget).data('quantity');
	var alim_id = $(e.relatedTarget).data('alim-id');
	$(e.currentTarget).find('h4[name="aliment_name"]').text("Aliment : " + alim_name);
	$(e.currentTarget).find('#quantity_value').val(alim_quantity);
	$(e.currentTarget).find('#quantity_id').val(alim_id);
    });
}

