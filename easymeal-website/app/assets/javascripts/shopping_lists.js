function valid_submit_form(){
    $("#name").change(function(){
	$("#name_error").addClass("hidden");
	$("#name_error").hide();
    });
 
    $("#new_shopping_list").submit(function(e){
	var value = true;
	if ($("#name").val().length == 0){
	    value = false;
	    $("#name_error").removeClass("hidden");
	    $("#name_error").show();
	}
	if (value == true)
	    $(this).submit(); 
	return false;
    });
}

